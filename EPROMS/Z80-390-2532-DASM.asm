	out    (CRTPRNTCNT),a         ; 00F0BC D3 83
	ex     af,af'          ; 00F0BE 08
	out    (CRTKEYBCNT),a         ; 00F0BF D3 87
	ex     af,af'          ; 00F0C1 08
	dec    a               ; 00F0C2 3D
	out    (CRTKEYBCNT),a         ; 00F0C3 D3 87
	ex     af,af'          ; 00F0C5 08
	out    (CRTSERVCNT),a         ; 00F0C6 D3 8B
	ld     a,$f1           ; 00F0C8 3E F1
	out    (CRTSERVCNT),a         ; 00F0CA D3 8B
