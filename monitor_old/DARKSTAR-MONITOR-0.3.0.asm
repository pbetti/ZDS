	LD	(IY + 0), C
	INC	IY
	INC	HL
	DJNZ	VDRSL1

	LD	C, VDRDSEC		; read command
	LD	(IY + 0), C
	LD	HL, FDRVBUF
	LD	C, (HL)			; drive
	LD	(IY + 1), C
	LD	BC, (FSECBUF)		; sector
	DEC	BC			; base sector # is zero...
	LD	(IY + 2), C
	LD	(IY + 3), B
	LD	BC, (FTRKBUF)		; track
	LD	(IY + 4), C
	LD	(IY + 5), B

