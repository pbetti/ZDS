	LD	BC, VDBUFSZ		; block size
	CALL	PSNDBLK			; send command block
	LD	A, C
	CP	$00			; what happens ?
	JR	Z, VDROK		; tx ok
	DEC	D			; retry ?
	JR	NZ, VDRTRY
	LD	A, 1			; ret tx err
	JR	VDRNOK
					; receive sector now
