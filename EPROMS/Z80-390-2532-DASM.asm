	ld     a,$0e           ; 00F060 3E 0E
	out    (CRT6545ADST),a         ; 00F062 D3 8C
	ld     a,h             ; 00F064 7C
	out    (CRT6545DATA),a         ; 00F065 D3 8D
	ld     a,$0f           ; 00F067 3E 0F
	out    (CRT6545ADST),a         ; 00F069 D3 8C
	ld     a,l             ; 00F06B 7D
	out    (CRT6545DATA),a         ; 00F06C D3 8D
SDCUAE1:
