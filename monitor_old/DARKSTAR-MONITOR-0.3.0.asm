;; VDSKRD - read a sector form remote
;;
;; use:
;;	none
;; unclean register usage: A, IY

VDSKRD:
	PUSH	IY
	PUSH	DE
	PUSH	BC
	PUSH	HL
	LD	D, 5			; retries
VDRTRY:	LD	IY, VDSKBUF
	LD	HL, S_VHDR
	LD	B, 4
